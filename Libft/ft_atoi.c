#include "libft.h"

int	ft_atoi(const char *str)
{
	int							i;
	unsigned long long			nb;
	int							neg;

	i = 0;
	nb = 0;
	neg = 1;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			neg *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb = (nb * 10) + (str[i] - '0');
		if (nb >= LLONG_MAX && neg == 1)
			return (-1);
		if (nb > LLONG_MAX && neg == -1)
			return (0);
		i++;
	}
	return (nb * neg);
}
